    private void escalarTexto() {
        int ancho = getWidth();
        int alto = getHeight();
        int tamañoBase = Math.max(12, Math.min(ancho, alto) / 25);
        int tamañoTitulo = tamañoBase + 2;

        Font fuenteTitulo = new Font("Segoe UI", Font.BOLD, tamañoTitulo);
        Font fuenteNormal = new Font("Segoe UI", Font.PLAIN, tamañoBase);

        jLabelTitulo1.setFont(fuenteTitulo);
        jLabelTitulo2.setFont(fuenteTitulo);
        jLabelEntrenamientos.setFont(fuenteNormal);
        jLabelIMC.setFont(fuenteNormal);

        JButton[] botones = { btnPrincipiante, btnIntermedio, btnAvanzado, btnIMC };
        for (JButton boton : botones) {
            boton.setFont(fuenteNormal);
        }
    }
    
