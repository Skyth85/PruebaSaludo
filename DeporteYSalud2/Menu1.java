   private void cargarImagenDesdeInternet() {
    try {
        URL url = new URL("https://img.freepik.com/psd-gratis/plantilla-banner-web-gimnasios-gimnasios_106176-674.jpg?semt=ais_hybrid&w=740&q=80");  // Pon aquí tu URL
        imagenOriginal = ImageIO.read(url);
        lblImagen = new JLabel();
        lblImagen.setHorizontalAlignment(SwingConstants.CENTER);
        lblImagen.setOpaque(false);

        // Añadir la etiqueta de imagen al panel
        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = 0;
        gbc.gridy = 0;
        gbc.gridwidth = 2;
        gbc.insets = new Insets(10, 10, 10, 10);
        gbc.fill = GridBagConstraints.BOTH;
        gbc.weightx = 1.0;
        gbc.weighty = 0.3;

        jPanel1.add(lblImagen, gbc);
    } catch (IOException e) {
        System.err.println("Error al cargar la imagen desde la URL: " + e.getMessage());
    }
}

// Método para actualizar el tamaño de la imagen
private void actualizarImagen() {
        if (imagenOriginal != null && lblImagen.getWidth() > 0 && lblImagen.getHeight() > 0) {
        Image imgEscalada = imagenOriginal.getScaledInstance(
            lblImagen.getWidth(),
            lblImagen.getHeight(),
            Image.SCALE_SMOOTH
        );
        lblImagen.setIcon(new ImageIcon(imgEscalada));
    }
    }

